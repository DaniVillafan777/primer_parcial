package com.emergentes.primer_parcial;

public class Metodos {
    private String materia[];
    private String nombre;
    private String e_primer;
    private String e_segundo;
    private String e_final;
    private int suma;
    public Metodos() {
    }

    public String[] getMateria() {
        return materia;
    }

    public void setMateria(String[] materia) {
        this.materia = materia;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getE_primer() {
        return e_primer;
    }

    public void setE_primer(String e_primer) {
        this.e_primer = e_primer;
    }

    public String getE_segundo() {
        return e_segundo;
    }

    public void setE_segundo(String e_segundo) {
        this.e_segundo = e_segundo;
    }

    public String getE_final() {
        return e_final;
    }

    public void setE_final(String e_final) {
        this.e_final = e_final;
    }

    public int getSuma() {
        return suma;
    }

    public void setSuma(int suma) {
        this.suma = suma;
    }
}
